    private void BtnAfficherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAfficherActionPerformed
        jTable1.removeAll();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.setRowCount(0);
        Vector<history> vh = new Vector<>();
        if(txtId1.getText().equals("")&&txtId2.getText().equals("")){
            vh=abonne_service.get_all_history();
            if(vh.isEmpty()){JOptionPane.showMessageDialog(null, "Wrong Information");}
        }
        else if((!txtId1.getText().equals("")) && txtId2.getText().equals(""))
        {
            vh=abonne_service.get_all_history(Integer.parseInt(txtId1.getText()));
            if(vh.isEmpty()){JOptionPane.showMessageDialog(null, "Wrong Information");}
        }
        else if(!txtId2.getText().equals("") && txtId1.getText().equals(""))
        {
            vh=abonne_service.get_all_history(Integer.parseInt(txtId2.getText()),"ha");
            if(vh.isEmpty()){JOptionPane.showMessageDialog(null, "Wrong Information");}
        }
        else if(!txtId2.getText().equals("") && !txtId1.getText().equals("")){
            vh=abonne_service.get_all_history(Integer.parseInt(txtId1.getText()),Integer.parseInt(txtId2.getText()));
            if(vh.isEmpty()){JOptionPane.showMessageDialog(null, "Wrong Information");}
        }
        else{
            
        }
        for(history h:vh)
        {
            Object objet[]={h.getAction_id(),h.getAction(),h.getDate_action(),h.getEmployee_id(),h.getAbonne_id()};
            model.addRow(objet);
        }
    }//GEN-LAST:event_BtnAfficherActionPerformed

