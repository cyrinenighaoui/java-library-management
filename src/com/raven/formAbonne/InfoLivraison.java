    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String adresseText = adresse.getText();
    String instructionText = instruction.getText();
    String numTelText = num_tel.getText();
    if (adresseText.isEmpty() || instructionText.isEmpty() || numTelText.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Veuillez remplir tous les champs.", "Erreur", JOptionPane.ERROR_MESSAGE);
        return; // Arrêter le traitement si un champ est vide
    }
    if (!numTelText.matches("\\d{8}")) {
        JOptionPane.showMessageDialog(null, "Le numéro de téléphone doit être composé de 8 chiffres.", "Erreur", JOptionPane.ERROR_MESSAGE);
        return; 
    }
    
    AbonneService a = new AbonneService();
    boolean test = a.ajouter_livraison(userID, ouvrage_id, adresseText, instructionText, Integer.parseInt(numTelText));
    if (test) {
        JOptionPane.showMessageDialog(null, "OK.", "", JOptionPane.INFORMATION_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(null, "Erreur.", "", JOptionPane.INFORMATION_MESSAGE);
    } 
 
    }//GEN-LAST:event_jButton1ActionPerformed

