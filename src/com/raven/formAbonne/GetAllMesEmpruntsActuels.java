    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(jTable1.getSelectedRowCount()==0){
            JOptionPane.showMessageDialog(null, "Veuillez sélectionner un emprunt.", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
        else if(jTable1.getSelectedRowCount()>1)
        {
            JOptionPane.showMessageDialog(null, "Veuillez sélectionner un seul emprunt et pas plusieurs.", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
        else{
            AbonneService as=new AbonneService();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            int id=Integer.parseInt(model.getValueAt(jTable1.getSelectedRow(),0).toString());
            if(as.getEmprunt(id).getEtat().equals("retourné")){
                JOptionPane.showMessageDialog(null, "Cet Ouvrage est déjà retourné", "Erreur", JOptionPane.DEFAULT_OPTION);
            }
            else{
                String x=as.retourner(as.getEmprunt(id));
                if(x.equals("Ouvrage retourné avec succès")){
                    JOptionPane.showMessageDialog(null, x, "succès", JOptionPane.DEFAULT_OPTION);
                }
                else{
                    JOptionPane.showMessageDialog(null, x, "Erreur", JOptionPane.ERROR_MESSAGE);
                }
                afficher();
            }}
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if(jTable1.getSelectedRowCount()==0){
            JOptionPane.showMessageDialog(null, "Veuillez sélectionner un emprunt.", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
        else if(jTable1.getSelectedRowCount()>1)
        {
            JOptionPane.showMessageDialog(null, "Veuillez sélectionner un seul emprunt et pas plusieurs.", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
        else{
            AbonneService as=new AbonneService();
            Emprunt e;
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            int id=Integer.parseInt(model.getValueAt(jTable1.getSelectedRow(),0).toString());
            if(as.getEmprunt(id).getEtat().equals("retourné")){
                JOptionPane.showMessageDialog(null, "Cet emprunt est déjà retourné", "Erreur", JOptionPane.DEFAULT_OPTION);
            }
            else if(as.getEmprunt(id).getEtat().equals("prolongé")){
                JOptionPane.showMessageDialog(null, "Cet emprunt est déjà prolongé, vous ne pouvez le prolonger encore", "Erreur", JOptionPane.DEFAULT_OPTION);
            }
            else{
                e=as.getEmprunt(id);
                e.setDate_fin_prevue(e.getDate_fin_prevue().plusDays(3).format(formatter).toString());
                e.setDate_fin_effective(e.getDate_fin_effective().plusDays(3).format(formatter).toString());
                String x=as.prolonger(e);
                if(x.equals("Emprunt prolongé de 3 jours")){
                    JOptionPane.showMessageDialog(null, x, "succès", JOptionPane.DEFAULT_OPTION);
                }
                else{
                    JOptionPane.showMessageDialog(null, x, "Erreur", JOptionPane.ERROR_MESSAGE);
                }
                afficher();
            }}
    }//GEN-LAST:event_jButton5ActionPerformed
