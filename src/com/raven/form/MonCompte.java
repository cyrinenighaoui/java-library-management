private void chargerDonneesEmploye() {
    PreparedStatement pstmt = null;
    ResultSet rs = null;

    try {
        Connection connection = LIBRAIRIE.getConnection();
        String sql = "SELECT nom, prenom, salaire, date_naissance, cin, image FROM employee WHERE employee_id = ?";
        pstmt = connection.prepareStatement(sql);
        pstmt.setInt(1, 20);
        rs = pstmt.executeQuery();
        if (rs.next()) {
            jTextField1.setText(rs.getString("nom"));
            jTextField2.setText(rs.getString("prenom"));
            jTextField3.setText(String.valueOf(rs.getInt("cin")));
            jTextField4.setText(String.valueOf(rs.getInt("salaire")));
            jTextField5.setText(rs.getDate("date_naissance").toString());

            byte[] imgBytes = rs.getBytes("image");
           if (imgBytes != null) {
    ImageIcon imageIcon = new ImageIcon(imgBytes);
    jLabel1.setIcon(imageIcon);
} else {
                System.out.println("Image non trouv√©e ou erreur de lecture.");
                jLabel1.setIcon(null);
            }
        }
    } catch (Exception e) {
        e.printStackTrace();
    } finally {
        try {
            if (rs != null) rs.close();
            if (pstmt != null) pstmt.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
}


