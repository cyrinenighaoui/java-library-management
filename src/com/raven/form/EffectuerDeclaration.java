    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        boolean  repond;
        GererLivraison g=new GererLivraison();

        if (jTextField2.getText().isEmpty()) {
         JOptionPane.showMessageDialog(null, "Veuillez saisir abonne_id", "Erreur", JOptionPane.ERROR_MESSAGE);
        } else if (jRadioButton2.isSelected() && jTextArea1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Veuillez saisir votre r√©clamation dans le champ 'Autre'", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
        else if(!g.id_abonne_existe(Integer.parseInt(jTextField2.getText())))
        {
            JOptionPane.showMessageDialog(null, "Veuillez saisir un abonne existant ", "Erreur", JOptionPane.ERROR_MESSAGE);

        }
        else if(jRadioButton1.isSelected() && jRadioButton2.isSelected())
                {
                 JOptionPane.showMessageDialog(null, "Choisir oui ou non", "Erreur", JOptionPane.ERROR_MESSAGE);
                }
        else if (!jRadioButton1.isSelected() && !jRadioButton2.isSelected())
        {
          JOptionPane.showMessageDialog(null, "Veuillez saisir oui ou non", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            if (jRadioButton1.isSelected())
            {
                repond=true;
            }
            else
            {
                repond=false;
            }
           Declaration d=new Declaration(Integer.parseInt(jTextField2.getText()),repond, jTextArea1.getText());
            boolean test=g.ajouter_declaration(d);
            if (test)
            {
              JOptionPane.showMessageDialog(null, "BIEN RECU", "Erreur", JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "ERREUR", "Erreur", JOptionPane.ERROR_MESSAGE);
            }
        }
       

        
     
     
    }//GEN-LAST:event_jToggleButton1ActionPerformed

